<h1>Missão</h1>
<img id="img_missao" src="imagens/missao.png"/>
<p>A missão é uma declaração concisa do propósito e das responsabilidades da sua empresa perante os seus clientes:</p>
<div class="lista">
    <ol>
        <li>Por que a empresa existe?</li>
        <li>O que a empresa faz?</li>
        <li>Para quem?</li>
    </ol>
</div> 
<p>A organização deverá seguir uma linha da qual não abre mão, como por exemplo:</p>
<div class="lista">
    <ul>
        <li>honestidade,</li>
        <li>respeito,</li>
        <li>qualidade,</li>
        <li>integridade,</li>
        <li>etc.</li>
    </ul>
</div>    
<p>Você é um empresário de visão, ou trabalha em uma empresa visionária?</p>
<p>Como você imagina que será sua empresa nos próximos dez ou vinte anos?</p>
<p>Estas perguntas podem ser respondidas se analisarmos o termo visão.</p>
