<h1>Visão</h1>
<img id="img_visao" src="imagens/visao.png" />
<p>A visão é a descrição do futuro desejado para a empresa. Esse enunciado reflete o alvo a ser procurado pelos esforçoes individuais, da equipe e 
    pela alocação de recursos.</p>
<p>A visão deve conter tanto a aspiração, como a inspiração. A aspiração de tornar-se "algo", 
    e a inspiração porque esse "algo" deve merecer e valer a pena ser concretizado.</p>
<p>Deve ser uma frase prática, realista e visível, pois não passará de uma mera alucinação,
    se ela sugerir ou propor resultados inatingíveis. Uma simples frase pode sintetizar essa idéia sendo, ao mesmo tempo, ambiciosa e inspiradora.</p>
<p>Deve facilitar as respostas para as seguintes perguntas:</p>
<div class="lista">
    <ul>
        <li>No que a empresa quer se tornar?</li>
        <li>Onde nós estaremos?</li>
        <li>O que a empresa será?</li>
        <li>Em que direção deve-se apontar os esforços dos dirigentes e colaboradores?</li>
    </ul>
</div>
