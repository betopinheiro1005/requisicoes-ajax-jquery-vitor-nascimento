<h1>Valores</h1>
<img id="img_valores" src="imagens/valores.png" />
<p>Quando falamos em valores, estamos falando sobre princípios, ou crenças, que servem de guia, ou critério,
    para os comportamentos, atitudes e decisões de todas e quaisquer pessoas, que no exercício das suas responsabilidades, e na busca dos seus objetivos, estejam executando a missão,
    na direção da visão.</p>
<p>Os valores também podem ser vistos como um conjunto de crenças, ou princípios, que definem e facilitam
    a participação das pessoas no desenvolvimento da missão, visão e dos próprios valores.</p>
<p>Os valores ainda facilitam no comprometimento entre os colaboradores e dos colaboradores com os clientes,
    comunidade e com a sociedade.</p>
<p>Resumidamente, os valores definem as regras básicas que norteiam os comportamentos e atitudes dos colaboradores.
    São regras para que, executando a missão, se alcance a visão.</p>
<p>Os valores dão o suporte, é o foco da moral e ética da empresa. Para exemplos de visão, missão e valores, entre em contato conosco.</p>
